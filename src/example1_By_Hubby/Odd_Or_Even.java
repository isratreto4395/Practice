public class Odd_Or_Even {

    public void oddNEven(int num) {
        if (num % 2 == 0) {
            System.out.println("Its a even number!!");
        } else if (num % 2 ==1) {
            System.out.println("Its a odd number!!!");
        }else {
            System.out.println("invalid input");
        }

    }



}


